#include <stdlib.h>
#include <stdio.h>

/**
 * main- program that prints its name
 *
 * @argc: holds the number of arguments
 * @argv: array pointer that holds the arguments
 *
 * Return: Always 0
 */

int main(int __attribute__((unused)) argc, char *argv[])
{
	int i = 0;

	printf("%s\n", argv[i]);
	return (0);
}
